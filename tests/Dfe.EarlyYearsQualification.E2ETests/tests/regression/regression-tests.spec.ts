const createScenario = (overrides: Partial<Scenario>): Scenario => ({
    scenarioName: "",
    scenarioId: 0,
    monthStarted: "",
    yearStarted: "",
    monthAwarded: "",
    yearAwarded: "",
    selectedLevel: 0,
    additionalRequirements: [],
    selectedAwardingOrganisation: "",
    qualificationToSelect: "",
    expectedContent: [],
    ...overrides
});

const createRatioRequirement = (
    level: string,
    approvalStatus: string,
    expectedContent = '',
): RatioRequirementContent => ({
    level,
    expectedContent,
    approvalStatus,
    headerId: `${level.replace(' ', '\\ ')}_heading`,
    contentId: `ratio-${level.replace(' ', '')}-additional-info`,
    approvalId: `ratio-${level.replace(' ', '')}-tag`,
});

const createExpectedContent = (
    fullAndRelevantHeading: string,
    fullAndRelevantContent: string,
    ratioRequirementContent: RatioRequirementContent[],
    staffChildsRatiosContent?: string,
): ExpectedContent => ({
    fullAndRelevantHeading,
    fullAndRelevantContent,
    staffChildsRatiosContent,
    ratioRequirementContent,
});

type ExpectedContentOverrides = {
    fullAndRelevantHeading?: string,
    fullAndRelevantContent?: string,
    staffChildsRatiosContent?: string,
}

const createSingleRatioExpectedContent = (
    level: string,
    approvalStatus: string,
    expectedContent = '',
    overrides: ExpectedContentOverrides = {},
): ExpectedContent[] => [
    createExpectedContent(
        overrides.fullAndRelevantHeading ?? qualificationFullAndRelevantHeading,
        overrides.fullAndRelevantContent ?? qualificationFullAndRelevantHeadingContent,
        [createRatioRequirement(level, approvalStatus, expectedContent)],
        overrides.staffChildsRatiosContent,
    ),
];

