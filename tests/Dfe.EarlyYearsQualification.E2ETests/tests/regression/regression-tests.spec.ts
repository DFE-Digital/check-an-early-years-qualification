regressionScenarios.forEach((scenario) => {
    test(`${scenario.scenarioId}. Qualification Level: ${scenario.selectedLevel} - Ratio ${scenario.scenarioName}`, async ({ context, page }) => {
        // Setup
        await startJourney(page, context);
        await checkingOwnQualificationOrSomeoneElsesPage(page, "#yes");
        await whereWasTheQualificationAwarded(page, "#england");
        await chooseStartDateOptionBasedOnDate(page, scenario.monthStarted, scenario.yearStarted);
        await whenWasQualificationAwarded(page, scenario.monthAwarded, scenario.yearAwarded);
        await whatLevelIsTheQualification(page, scenario.selectedLevel);
        if (scenario.selectedAwardingOrganisation === "Not on the list") {
            await selectNotOnTheListAsTheAwardingOrganisation(page);
        }
        else {
            await whatIsTheAwardingOrganisationValue(page, scenario.selectedAwardingOrganisation);
        }
        await checkYourAnswersPage(page);
        await selectQualification(page, scenario.qualificationToSelect);
        await confirmQualification(page, "#yes");

        if (scenario.additionalRequirements.length > 0) {
            for (const x of scenario.additionalRequirements) {
                await processAdditionalRequirement(page, scenario.qualificationToSelect, x.index, x.answer);
            }
            await confirmAdditonalRequirementsAnswers(page, scenario.qualificationToSelect);
        }

        // Assertions
        await checkDetailsPage(page, scenario.qualificationToSelect);

        for (const content of scenario.expectedContent) {

            await checkText(page, `#qualification-result-message-heading`, content.fullAndRelevantHeading, null, true);
            await checkText(page, `#qualification-result-message-body`, content.fullAndRelevantContent, null, true);

            for (const ratioRequirement of content.ratioRequirementContent) {
                await checkText(page, `#${ratioRequirement.headerId}`, ratioRequirement.level, null, true);

                await checkText(page, `#${ratioRequirement.approvalId}`, ratioRequirement.approvalStatus, null, true);

                if (ratioRequirement.expectedContent !== "") {
                    await checkText(page, `#${ratioRequirement.contentId}`, ratioRequirement.expectedContent, null, true);
                }
                else {
                    await doesNotExist(page, `#${ratioRequirement.contentId}`);
                }
            }

            if (content.staffChildsRatiosContent !== undefined) {
                await checkText(page, 'h2#ratio-heading + p', content.staffChildsRatiosContent);

                if (scenario.selectedLevel != 2) {
                    if (await page.locator('#ratio-additional-info').count() > 0) {
                        await checkText(page, '#ratio-additional-info', ratioRequirmentAlternativeRouteContent, null, true);
                    }
                    else {
                        await checkText(page, 'h2#ratio-heading + p + p', ratioRequirmentAlternativeRouteContent, null, true);
                    }
                }
            }
        }
    });
});
