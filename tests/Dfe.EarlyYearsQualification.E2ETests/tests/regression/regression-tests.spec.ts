type Scenario = {
    scenarioName: string,
    scenarioId: number,
    monthStarted: string,
    yearStarted: string,
    monthAwarded: string,
    yearAwarded: string,
    selectedLevel: number,
    selectedAwardingOrganisation: string,
    qualificationToSelect: string,
    additionalRequirements: AdditionalRequirement[]
    expectedContent: ExpectedContent[]
}

type ExpectedContent = {
    fullAndRelevantHeading: string,
    fullAndRelevantContent: string,
    staffChildsRatiosContent?: string,
    ratioRequirementContent: RatioRequirementContent[],
}

type RatioRequirementContent = {
    level: string,
    expectedContent: string,
    approvalStatus: string,
    headerId: string,
    contentId: string,
    approvalId: string
}

type AdditionalRequirement = {
    index: number,
    answer: string
}

